//Array Sortout and reverse the Sorted Array
var array=[1,2,3,8,5,7,4,9];
//push()
array.push(6);//Added the 6 element in end of the array
console.log(array);//print the Array where 6 added in the end of the array
//sort()
console.log(array.sort());//Element of the Array put in the aending order 
//reverse()
console.log(array.reverse());//Array Reverse
//pop()
array.pop();//remove the last element in the array
console.log(array);//print the Array
//shift()
array.shift();// Remove Starting element in the Array and shift the whole Array to Left
console.log(array);
//unshift()
array.unshift(0);// Add the 0 element in the starting of the Array And shift the whole Array to Right
console.log(array);
